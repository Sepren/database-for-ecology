def extract_all_tags(text, patterns_dict):
    """ Находит ВСЕ совпадения в тексте """
    if not isinstance(text, str): return ""
    text_lower = text.lower()
    found = set()
    for tag, keywords in patterns_dict.items():
        for k in keywords:
            if k.lower() in text_lower:
                found.add(tag)
                break
    return ", ".join(list(found)) if found else "Прочее"